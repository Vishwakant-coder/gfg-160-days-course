class Solution {
  public:
    // Function to check if two strings are rotations of each other or not.
    bool areRotations(string &s1, string &s2) {
        // Your code here
        string str = s1+s1;
        int j=0;
        if(s1.size()!=s2.size())
            return false;
        for(int i=0; i<str.size();i++){
            if(s2[j] == str[i]) 
                j++;
        }
        return j==s2.size();
    }
};

