class Solution {
  public:
    int maximumProfit(vector<int> &p) {
        // code here
        int maxProfit = 0;
        int minStock = p[0];
        for(int i =1;i<p.size();i++){
            minStock = min(minStock , p[i]);
            maxProfit = max(maxProfit,p[i] - minStock);
        }
        return maxProfit;
    }
};


