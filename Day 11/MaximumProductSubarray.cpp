class Solution {
  public:
    // Function to find maximum product subarray
    int maxProduct(vector<int> &arr) {
        // Your Code Here
        int n = arr.size();
        int realMax = arr[0];
        int pre=1, suff=1;
        
        for(int i=0;i<n; i++){
            if(pre == 0)
                pre = 1;
            if(suff == 0)
                suff=1;
            pre *= arr[i];
            suff *= arr[n-i-1];
            realMax = max(realMax, max(pre, suff));
        }
        
        return realMax;
    }
};

